/**
 * As the name suggests, this class represents game 
 * personalities (Lord Vetinaari etc.).
 */
package bootstrap;

public class PersonalityCard implements Card {

	private String title;

	@Override
	public String getTitle() {
		return this.title;
	}

	@Override
	public void setTitle(String title) {
		this.title = title;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((title == null) ? 0 : title.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (!(obj instanceof PersonalityCard)) {
			return false;
		}
		PersonalityCard other = (PersonalityCard) obj;
		if (title == null) {
			if (other.title != null) {
				return false;
			}
		} else if (!title.equals(other.title)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "PersonalityCard[ " + title + " ]";
	}

}
